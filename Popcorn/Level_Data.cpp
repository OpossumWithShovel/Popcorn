ALevel_Data::ALevel_Data(int level_number)
	: Level(0)
{
	if (level_number < 1 || level_number > Max_Level_Number)
		AsTools::Throw();

	switch (level_number)
	{
	case 1:
		Level = (char *)Level_01;
		break;

	case 2:
		Level = (char *)Level_02;
		break;

	case 3:
		Level = (char *)Level_03;
		break;

	case 4:
		Level = (char *)Level_04;
		break;

	default:
		AsTools::Throw();
	}
}
